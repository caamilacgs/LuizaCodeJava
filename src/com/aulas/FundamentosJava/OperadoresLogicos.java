package com.aulas.FundamentosJava;

public class OperadoresLogicos {

    public static void main(String[]args) {

        if ((1 == (2 - 1)) && (2 == (1 + 1))) {
            System.out.println("Ambas as expressões são verdadeiras");
        }

        System.out.println("&& E CONDICIONAL: asduas expressões sejam verdadeiras.");
        System.out.println("|| OU CONDICIONAL: pelo meno um das expressões seja verdadeira.");
        System.out.println("! NÃO: Negação.");

    }
}
